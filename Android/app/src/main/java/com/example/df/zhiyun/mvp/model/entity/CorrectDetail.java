package com.example.df.zhiyun.mvp.model.entity;

public class CorrectDetail {
    String studentHomeWorkId;
    String homeworkName;
    long time;
    int noStudentCount;
    int studentCount;
    String realName;
    String className;
    float score;
    int type;
    float studentHomeworkScore;
    int types;
    int identity;
    float average;

    public String getStudentHomeWorkId() {
        return studentHomeWorkId;
    }

    public void setStudentHomeWorkId(String studentHomeWorkId) {
        this.studentHomeWorkId = studentHomeWorkId;
    }

    public String getHomeworkName() {
        return homeworkName;
    }

    public void setHomeworkName(String homeworkName) {
        this.homeworkName = homeworkName;
    }

    public long getTime() {
        return time;
    }

    public void setTime(long time) {
        this.time = time;
    }

    public int getNoStudentCount() {
        return noStudentCount;
    }

    public void setNoStudentCount(int noStudentCount) {
        this.noStudentCount = noStudentCount;
    }

    public int getStudentCount() {
        return studentCount;
    }

    public void setStudentCount(int studentCount) {
        this.studentCount = studentCount;
    }

    public String getRealName() {
        return realName;
    }

    public void setRealName(String realName) {
        this.realName = realName;
    }

    public String getClassName() {
        return className;
    }

    public void setClassName(String className) {
        this.className = className;
    }

    public float getScore() {
        return score;
    }

    public void setScore(float score) {
        this.score = score;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public float getStudentHomeworkScore() {
        return studentHomeworkScore;
    }

    public void setStudentHomeworkScore(float studentHomeworkScore) {
        this.studentHomeworkScore = studentHomeworkScore;
    }

    public int getTypes() {
        return types;
    }

    public void setTypes(int types) {
        this.types = types;
    }

    public int getIdentity() {
        return identity;
    }

    public void setIdentity(int identity) {
        this.identity = identity;
    }

    public float getAverage() {
        return average;
    }

    public void setAverage(float average) {
        this.average = average;
    }
}
